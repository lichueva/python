# Запит користувача на введення числа a та b
a = float(input("Введіть число a (додатнє): "))
b = float(input("Введіть число b (додатнє): "))

# Перевірка на додатність введених чисел
if a <= 0 or b <= 0:
    print("Числа a та b повинні бути додатніми.")
else:
    # Обчислення значення X відповідно до умови
    if a > b:
        X = a * b - 1
    elif a == b:
        X = 255
    else:
        X = (a - 5) / b

    # Виведення результату
    print("Значення X: ", X)
